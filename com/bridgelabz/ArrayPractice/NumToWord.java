package com.bridgelabz.ArrayPractice;

public class NumToWord {
    public  static void main(String args[])
    {
        int number =44;

        switch (number)
        {
            case 48:
                System.out.println("large");
                break;
            case 44:
                System.out.println("small");
                break;
            default:
                System.out.println("value is not in range");
                break;
        }

    }
}
